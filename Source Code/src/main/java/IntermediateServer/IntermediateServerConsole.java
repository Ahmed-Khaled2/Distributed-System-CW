    public void UpdateQueue(ArrayList<CustomerRequests> CustomerQueue) {
        this.CustomerQueue = CustomerQueue;
        UpdateTable();
    }

    public void UpdateTable() {
        DefaultTableModel model = (DefaultTableModel) CustomerTable.getModel();
        model.setRowCount(0);
        for (CustomerRequests cr : CustomerQueue) {
            model.addRow(new Object[]{cr.getID(), cr.getName(), cr.getViewing(), cr.getStatus()});
        }
    }

